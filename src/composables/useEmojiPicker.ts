// Build searchable emoji items from raw data
function buildEmojiItems(): EmojiItem[] {
  const items: EmojiItem[] = []
  
  for (const [category, emojis] of Object.entries(EMOJI_DATA)) {
    for (const emoji of emojis) {
      items.push({
        id: emoji,
        value: emoji,
        type: 'emoji',
        keywords: [emoji], // Basic - could be enhanced with actual keyword data
        category,
      })
    }
  }
  
  return items
}

// ============================================
// Composable
// ============================================

export function useEmojiPicker(config: EmojiPickerConfig = {}) {
  const {
    categories = DEFAULT_CATEGORIES,
    customItems = [],
    getFavorites = () => [],
    saveFavorite,
    maxFavorites = 16,
    columns = 8,
  } = config
  
  // State
  const isOpen = ref(false)
  const searchQuery = ref('')
  const activeCategory = ref(categories[0]?.id || 'smileys')
  const selectedSkinTone = ref(0)
  
  // Emoji data
  const allEmojis = shallowRef<EmojiItem[]>(buildEmojiItems())
  
  // Combined items (built-in + custom)
  const allItems = computed(() => [...allEmojis.value, ...customItems])
  
  // Favorites from external source
  const favorites = computed(() => {
    const favIds = getFavorites()
    return allItems.value
      .filter(item => favIds.includes(item.id))
      .slice(0, maxFavorites)
  })
  
  // Search results
  const searchResults = computed(() => {
    const query = searchQuery.value.toLowerCase().trim()
    if (!query) return []
    
    return allItems.value.filter(item => {
      // Match by value (emoji character)
      if (item.value.includes(query)) return true
      // Match by keywords
      return item.keywords.some(kw => kw.toLowerCase().includes(query))
    })
  })
  
  // Items for current category
  const categoryItems = computed(() => {
    if (activeCategory.value === 'favorites') {
      return favorites.value
    }
    return allItems.value.filter(item => item.category === activeCategory.value)
  })
  
  // What to display in the grid
  const displayItems = computed(() => {
    if (searchQuery.value.trim()) {
      return searchResults.value
    }
    return categoryItems.value
  })
  
  // Actions
  function open() {
    isOpen.value = true
    searchQuery.value = ''
  }
  
  function close() {
    isOpen.value = false
  }
  
  function selectItem(item: EmojiItem) {
    // Save to favorites if callback provided
    if (saveFavorite) {
      saveFavorite(item.id)
    }
    return item
  }
  
  function setCategory(categoryId: string) {
    activeCategory.value = categoryId
    searchQuery.value = ''
  }
  
  function setSearch(query: string) {
    searchQuery.value = query
  }
  
  return {
    // State
    isOpen,
    searchQuery,
    activeCategory,
    selectedSkinTone,
    
    // Data
    categories,
    allItems,
    favorites,
    displayItems,
    searchResults,
    columns,
    
    // Actions
    open,
    close,
    selectItem,
    setCategory,
    setSearch,
  }
}


